# Arithmetic
print('### Arithmetic operators ###\n')
# divide I: results into float
a = 5 / 2
print("5 / 2 = ", a)

# divide II: results into whole number
a = 5.0 // 2
print("5.0 // 2 = ", a)

a = 5 // 2.0
print("5 // 2.0 = ", a)

# exponent
a = 5 ** 3
print("5 ** 3 = ", a)

# ============================================
# Logical operators
print('\n### Logical operators ###\n')
x = True
y = False

print('x and y = ', x and y)
print('x or y = ', x or y)
print('not x = ', not x)
